#include "libpipex.h"

void	del_matrix(char **matrix)
{
	char	**tmp;

	tmp = matrix;
	while (*tmp != 0)
	{
		free(*tmp);
		tmp++;
	}
	free(*tmp);
	free(matrix);
}
